"use strict";
var express = require('express');
var AccountsController = require('./AccountsController');
var checkAuth = require('../../auth/check-auth');
var router = express.Router();
router.post('/login', AccountsController.login);
router.post('/logout', AccountsController.logout);
router.post('/register', AccountsController.createAccount);
router.patch('/reset-password', AccountsController.resetPassword);
router.get('/', checkAuth, AccountsController.getAccount);
router.patch('/update-email', checkAuth, AccountsController.updateEmail);
router.patch('/update-password', checkAuth, AccountsController.updatePassword);
router.delete('/', checkAuth, AccountsController.deleteAccount);
module.exports = router;
